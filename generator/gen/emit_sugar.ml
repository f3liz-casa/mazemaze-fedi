(* Emit_sugar : Ir.t -> misskey.ml, the Melange-natural convenience layer.

   The flat generated send takes an all-`option` request record, which is verbose
   to build by hand. Here we emit, per op, a wrapper with LABELED OPTIONAL
   arguments — `Notes.create client ~text:"hi" ()` — which is the idiomatic OCaml
   surface. It's mechanical: each `?field` argument PUNS straight onto the record
   field, and a Client carries the transport so it isn't threaded per call. This
   is the canonical natural API; the ReScript/TS surfaces mirror it. *)

open Ir

let buf_add = Buffer.add_string

(* the Client: origin/token + an injected json POST -> the transport every send
   wants. The small runtime conversion (prefix /api, inject the token as misskey's
   `i`) lives here; the real fetch is injected so this stays platform-agnostic. *)
let client_module =
  {ocaml|module Client = struct
  type t = { transport : string -> string -> Js.Json.t -> Js.Json.t Js.Promise.t }

  (* post : method -> url -> body -> json promise  (inject your platform's fetch) *)
  let make ~origin ?token ~post () =
    let transport meth path body =
      let body =
        match token with
        | None -> body
        | Some t ->
          let d =
            match Js.Json.decodeObject body with
            | Some d -> d
            | None -> Js.Dict.empty ()
          in
          Js.Dict.set d "i" (Js.Json.string t);
          Js.Json.object_ d
      in
      post meth (origin ^ "/api" ^ path) body
    in
    { transport }
end

|ocaml}

(* derive a short action name: strip the leading HTTP method and the tag segment
   from the camelCase opId (postNotesCreate + tag Notes -> create). *)
let methods = [ "post"; "get"; "put"; "delete"; "patch" ]

let strip_lead s lead =
  let ll = String.length lead and ls = String.length s in
  if ls > ll && String.lowercase_ascii (String.sub s 0 ll) = lead then
    Some (Repr.lower_first (String.sub s ll (ls - ll)))
  else None

let action_name ~tag name =
  let after_method = List.find_map (strip_lead name) methods |> Option.value ~default:name in
  match strip_lead after_method (String.lowercase_ascii tag) with
  | Some s when s <> "" -> s
  | _ -> after_method

let safe_ident s = if List.mem s Repr.keywords then s ^ "_" else s

(* one op wrapper. `action` is the chosen let-binding name. *)
let op_wrapper b action (o : op) =
  let send = Repr.lower_first o.name ^ "_send" in
  match o.request with
  | [] ->
    buf_add b (Printf.sprintf
      "  let %s (client : Client.t) () = Endpoints.%s client.transport ()\n" action send)
  | fields ->
    let labels = List.map (fun (f : field) -> fst (Repr.ocaml_label f.name)) fields in
    (* required field -> mandatory labeled arg (~x); optional -> ?x. Either way
       the label puns onto the record field of the matching option-ness. *)
    let args =
      List.map (fun (f : field) ->
        let l = fst (Repr.ocaml_label f.name) in
        (if Repr.field_optional f then "?" else "~") ^ l)
        fields
      |> String.concat " "
    in
    let puns = String.concat "; " labels in
    let m = Repr.module_name o.name in
    buf_add b (Printf.sprintf
      "  let %s (client : Client.t) %s () =\n    Endpoints.%s client.transport { Endpoints.%s.%s }\n"
      action args send m puns)

let emit (ir : t) : (string * string) list =
  let b = Buffer.create 262144 in
  buf_add b "(* Generated by the OCaml generator. Do not edit. *)\n";
  buf_add b "(* Melange-natural convenience layer: labeled-optional wrappers + Client. *)\n\n";
  buf_add b client_module;
  let tags =
    List.fold_left (fun acc o -> if List.mem o.tag acc then acc else o.tag :: acc) [] ir.ops
    |> List.sort_uniq compare
  in
  List.iter
    (fun tag ->
      buf_add b (Printf.sprintf "module %s = struct\n" tag);
      let seen = Hashtbl.create 64 in
      List.iter
        (fun o ->
          if o.tag = tag then begin
            (* collision guard: fall back to the full op name within the tag *)
            let base = safe_ident (action_name ~tag o.name) in
            let action =
              if Hashtbl.mem seen base then Repr.lower_first o.name else base
            in
            Hashtbl.replace seen action ();
            op_wrapper b action o
          end)
        ir.ops;
      buf_add b "end\n\n")
    tags;
  [ ("misskey.ml", Buffer.contents b) ]
